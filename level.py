def makelevel(level):
    walls = pygame.sprite.Group()
    levelchosen = Level1
    if level == 2: levelchosen=Level2
    if level == 3: levelchosen=Level3
    x = 0
    y = 0
    for i in levelchosen:
        for j in i:
            if j==1:
                walls.add(Wall((x*20 +10, y*20 +90)))
            x += 1
        x = 0
        y += 1
    return walls
