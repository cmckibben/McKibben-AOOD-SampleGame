def make_level(level):
    walls = pygame.sprite.Group()
    level_chosen = Level1
    if level == 2: level_chosen=Level2
    if level == 3: level_chosen=Level3
    x = 0
    y = 0
    for i in level_chosen:
        for j in i:
            if j==1:
                walls.add(Wall((x*20 +10, y*20 +90)))
            x += 1
        x = 0
        y += 1
    return walls
